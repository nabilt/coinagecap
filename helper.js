function request(url, callback, errCallback) {
    var xhr = new XMLHttpRequest();
    xhr.onreadystatechange = (function(myxhr) {
        return function() {
            if(myxhr.readyState === XMLHttpRequest.DONE) {
                callback(myxhr);
            } else {
                errCallback(myxhr);
            }
        }
    })(xhr);
    xhr.open('GET', url, true);
    xhr.send();
}

