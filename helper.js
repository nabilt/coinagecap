function millify(num) {
    var suffix = ['','K','M','B','T'];
    if (num < 1000) {
        return num;
    } else if (num < 1000000) {
        return String((num/1000).toFixed(2)) + 'K';
    } else if (num < 1000000000) {
        return String((num/1000000).toFixed(2)) + 'M';
    } else if (num < 1000000000000) {
        return String((num/1000000000).toFixed(2)) + 'B';
    } else if (num < 1000000000000000) {
        return String((num/1000000000000).toFixed(2)) + 'T';
    }
}

function getCoinParameter(userCoins, id, param) {
    if (typeof(userCoins[id]) == "undefined") {
        return false;
    } else {
        //console.log(id, userCoins[id][param]);
        return typeof(userCoins[id][param]) == "undefined" ? false : userCoins[id][param];
    }
}

function addTickerToModel(jsonTicker, tickerModel, removeOldItems) {
    if (typeof(removeOldItems) != "undefined" && removeOldItems == true)
        tickerModel.beginInsert();

    var updatedItems = []
    for (var i = 0; i < jsonTicker.length; i++) {
        var ticker = jsonTicker[i];
        // QML doesn't like variables that start with numbers
        ticker['day_volume_usd'] = Number(ticker['24h_volume_usd']);

        // Convert strings to numbers
        ticker['rank'] = Number(ticker['rank']);
        ticker['price_usd'] = Number(ticker['price_usd']);
        ticker['price_btc'] = Number(ticker['price_btc']);
        ticker['market_cap_usd'] = Number(ticker['market_cap_usd']);
        ticker['available_supply'] = Number(ticker['available_supply']);
        ticker['total_supply'] = Number(ticker['total_supply']);
        ticker['max_supply'] = Number(ticker['max_supply']);
        ticker['percent_change_1h'] = Number(ticker['percent_change_1h']);
        ticker['percent_change_24h'] = Number(ticker['percent_change_24h']);
        ticker['percent_change_7d'] = Number(ticker['percent_change_7d']);
        ticker['last_updated'] = Number(ticker['last_updated']);

        var updated = tickerModel.addItem(ticker);
        if (updated)
            updatedItems.push(i);
    }

    if (typeof(removeOldItems) != "undefined" && removeOldItems == true)
        tickerModel.endInsert();

    return updatedItems;
}

